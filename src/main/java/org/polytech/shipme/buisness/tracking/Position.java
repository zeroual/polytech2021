package org.polytech.shipme.buisness.tracking;

public class Position {
    private float lat;
    private float lng;

    public Position() {
    }

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public void setLng(float lng) {
        this.lng = lng;
    }

    public float getLng() {
        return lng;
    }
}
